public class _p40_Numeros100a1 {
    public static void main(String[] args) {
        int c;

        c=100;
        
        while( c >= 1 ) {
            System.out.print(c+" ");
            c--;
        }

        System.out.println("\nProceso terminado ...");
    }
}
